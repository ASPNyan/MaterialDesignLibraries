namespace MaterialDesign.Icons;

/// <summary>
/// Provides enum identifiers for the three different types of Material Icon line styles.
/// </summary>
public enum MdIconLineStyle
{
    /// <summary>
    /// Outlined Symbols are a light, clean style that works well in dense UIs.
    /// The stroke weight of outlined icons can be adjusted to complement or contrast the type weight you’ve chosen.
    /// </summary>
    Outlined,
    /// <summary>
    /// Rounded Symbols use a corner radius that pairs well with brands that use heavier typography,
    /// curved logos, or circular elements to express their style.
    /// </summary>
    Rounded,
    /// <summary>
    /// Sharp Symbols use corners and straight edges for a crisp, rectangular style that remains legible even at smaller scales.
    /// </summary>
    Sharp
}