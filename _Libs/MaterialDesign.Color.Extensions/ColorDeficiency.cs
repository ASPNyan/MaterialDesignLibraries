namespace MaterialDesign.Color.Extensions;

/// <summary>
/// Provides enum identifiers for the three groups of color vision deficiency (CVD) types: Protan-, Deutan-, and Tritan-
/// </summary>
public enum ColorDeficiency
{
    /// <summary>
    /// A CVD type where those afflicted are unable to perceive red light
    /// </summary>
    Protan,
    /// <summary>
    /// A CVD type where those afflicted are unable to perceive green light
    /// </summary>
    Deutan,
    /// <summary>
    /// A CVD type where those afflicted are unable to perceive blue light
    /// </summary>
    Tritan
}